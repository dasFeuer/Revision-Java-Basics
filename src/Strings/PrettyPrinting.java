package Strings;

public class PrettyPrinting {
    static void main(String[] args) {
        float a = 243.5231f;

        System.out.printf("Formatted number is %.2f \n", a);
        System.out.printf("Pie number is %.3f\n", Math.PI);
        System.out.printf("Hello! I am learning %s and %s as well.", "Java", "Spring Boot");
    }
}
